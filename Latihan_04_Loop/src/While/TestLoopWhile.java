    public static void main(String[] args) {
        int i=1; // i=1 ialah batas bawah(nilai awal)
        while (i < 9) {    //9 merupakan batas atas , dengan logika kurang dari
            System.out.println("Nilai " + i);
            i++;
            
        }
    }
    
