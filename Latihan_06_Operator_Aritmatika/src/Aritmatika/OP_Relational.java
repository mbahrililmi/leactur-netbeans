public class OP_Relational {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        int A = 9, B=1;
        //==, !=, >, <, >=, <=
        System.out.println("A==B \t" + (A==B));
        System.out.println("A!=B \t" + (A!=B));
        System.out.println("A>B \t" + (A>B));
        System.out.println("A<B \t" + (A<B));
        System.out.println("A>=B \t" + (A>=B));
        System.out.println("A<=B \t" + (A<=B));
        
    }
    
}
